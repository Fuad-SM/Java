    private void simpaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpaButtonActionPerformed
       if(nisTxt.getText().equals("")|| nisnTxt.getText().equals("")|| namaTxt.getText().equals("")||
          tempatLahirTxt.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Tolong Jangan Dikosongkan!",
                    "Peringatan",JOptionPane.ERROR_MESSAGE);}
       else{
       String gender = null;
            if (lakiButton.isSelected()){
                gender = "L";
            }
            else if (perempuanButton.isSelected()){
                gender = "P";
            }
    
//        java.sql.Date tanggal = new java.sql.Date(tanggalLahirCalender.getDate().getTime());
//      String tanggal = null;  
        
           int pilihan = JOptionPane.showConfirmDialog(this, "Simpan data?", "Simpan data siswa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);    
        if(pilihan == JOptionPane.YES_OPTION){
            sw.inputSiswa(nisTxt.getText(),
                    nisnTxt.getText(),
                    namaTxt.getText(),
                    tempatLahirTxt.getText(), 
                    tanggalLahirCalender.getDateFormatString(),
                    gender,
                    alamatTxt.getText());
        }
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
       }
    clear();    
    }//GEN-LAST:event_simpaButtonActionPerformed

    

