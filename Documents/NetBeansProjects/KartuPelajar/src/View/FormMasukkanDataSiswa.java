      void filterNis(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Pada Kolom NIS Hanya Bisa Memasukan Karakter Angka", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }
      void filterNisn(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Pada Kolom NISN Hanya Bisa Memasukan Karakter Angka");
        }
    }
      
